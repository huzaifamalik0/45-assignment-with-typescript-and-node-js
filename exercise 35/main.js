var pet_animals = ["CAT ", "DOG", "RABBIT"];
//Using FOR-loop
for (var _i = 0, pet_animals_1 = pet_animals; _i < pet_animals_1.length; _i++) {
    var onePet = pet_animals_1[_i];
    console.log("A ".concat(onePet, " WOULD MAKE A GREAT PET."));
}
